#!/usr/bin/env node
/**
 * ONE-OFF build tool — NOT a runtime/dev dependency of this project.
 * Regenerates src/components/Signature/signaturePaths.generated.ts from the
 * Pacifico font. Re-run whenever siteConfig.name changes.
 *
 * Usage:
 *   npm install --no-save opentype.js
 *   node scripts/generate-signature-paths.mjs
 *   npm prune   # removes the temporary opentype.js install again
 */
import opentype from 'opentype.js';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';

const WORD = 'Magical_ball'; // must match siteConfig.name exactly
const FONT_URL = 'https://raw.githubusercontent.com/google/fonts/main/ofl/pacifico/Pacifico-Regular.ttf';
const FONT_SIZE = 200; // arbitrary unit space; real size comes from CSS via viewBox scaling
const DECIMALS = 2; // path data rounding
const UNDERSCORE_GAP = FONT_SIZE * 0.35; // extra breathing room around the underscore glyph
const VIEWBOX_PADDING = 12; // guards against stroke-width/swash clipping

async function main() {
	const res = await fetch(FONT_URL);
	if (!res.ok) throw new Error(`Failed to fetch Pacifico font: ${res.status}`);
	const font = opentype.parse(await res.arrayBuffer());

	const baselineY = FONT_SIZE * 0.75;
	const letters = []; // { char, glyphPath }

	// Split into contiguous alphabetic runs around the underscore so each run gets a
	// real kerning-aware layout pass via getPaths(), while the underscore gets a
	// manual extra gap instead of being kerned tightly against its neighbours.
	const segments = WORD.split(/(_)/).filter(Boolean); // -> ['Magical', '_', 'ball']
	let cursorX = 0;

	for (const segment of segments) {
		if (segment === '_') {
			letters.push({ char: '_', glyphPath: font.getPath('_', cursorX, baselineY, FONT_SIZE) });
			cursorX += font.getAdvanceWidth('_', FONT_SIZE) + UNDERSCORE_GAP;
			continue;
		}
		const glyphPaths = font.getPaths(segment, cursorX, baselineY, FONT_SIZE, { kerning: true });
		segment.split('').forEach((char, i) => letters.push({ char, glyphPath: glyphPaths[i] }));
		cursorX += font.getAdvanceWidth(segment, FONT_SIZE, { kerning: true });
	}

	// Union bounding box across every letter drives the shared viewBox.
	let minX = Infinity;
	let minY = Infinity;
	let maxX = -Infinity;
	let maxY = -Infinity;
	for (const { glyphPath } of letters) {
		const bbox = glyphPath.getBoundingBox();
		minX = Math.min(minX, bbox.x1);
		minY = Math.min(minY, bbox.y1);
		maxX = Math.max(maxX, bbox.x2);
		maxY = Math.max(maxY, bbox.y2);
	}
	minX -= VIEWBOX_PADDING;
	minY -= VIEWBOX_PADDING;
	maxX += VIEWBOX_PADDING;
	maxY += VIEWBOX_PADDING;

	const signaturePaths = letters.map(({ char, glyphPath }) => ({ char, d: glyphPath.toPathData(DECIMALS) }));
	const viewBox = { minX, minY, width: maxX - minX, height: maxY - minY };

	const out = `// AUTO-GENERATED by scripts/generate-signature-paths.mjs — do not hand-edit.
// Regenerate: npm install --no-save opentype.js && node scripts/generate-signature-paths.mjs && npm prune
export interface SignatureLetterPath {
	char: string;
	d: string;
}

// The font size (in the same abstract units as the path/viewBox coordinates)
// used to generate these paths. SIGNATURE_VIEWBOX.height / SIGNATURE_FONT_SIZE
// gives the ratio between the glyphs' actual ink height and a CSS font-size —
// used by Signature.astro to size the SVG so it visually matches what the same
// word would look like set in Pacifico at that font-size.
export const SIGNATURE_FONT_SIZE = ${FONT_SIZE} as const;

export const SIGNATURE_VIEWBOX = ${JSON.stringify(viewBox)} as const;

export const signaturePaths: SignatureLetterPath[] = ${JSON.stringify(signaturePaths, null, 2)};
`;

	const outPath = fileURLToPath(new URL('../src/components/Signature/signaturePaths.generated.ts', import.meta.url));
	writeFileSync(outPath, out, 'utf-8');
	console.log(`Wrote ${letters.length} letter paths to ${outPath}`);
}

main().catch((err) => {
	console.error(err);
	process.exit(1);
});
